package io.apps4u.fpdatabase;

public class SignUp {
    private String _legajo;

    private String _latitude;

    private String _longitude;

    private String _details;

    private String _address;

    private String _timestamp;

    public String get_legajo() {
        return _legajo;
    }

    public void set_legajo(String _legajo) {
        this._legajo = _legajo;
    }

    public String get_latitude() {
        return _latitude;
    }

    public void set_latitude(String _latitude) {
        this._latitude = _latitude;
    }

    public String get_longitude() {
        return _longitude;
    }

    public void set_longitude(String _longitude) {
        this._longitude = _longitude;
    }

    public String get_details() {
        return _details;
    }

    public void set_details(String _details) {
        this._details = _details;
    }

    public String get_address() {
        return _address;
    }

    public void set_address(String _address) {
        this._address = _address;
    }

    public String get_timestamp() {
        return _timestamp;
    }

    public void set_timestamp(String _timestamp) {
        this._timestamp = _timestamp;
    }
}
